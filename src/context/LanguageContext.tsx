const LanguageContext = createContext(null)

// Provider component
export const LanguageProvider = ({ children }) => {
  // Load saved language from localStorage or detect browser language
  const getSavedLanguage = () => {
    try {
      const saved = localStorage.getItem('esim_language')
      if (saved && languages[saved]) {
        return saved
      }
    } catch (error) {
      console.log('Could not load saved language')
    }
    
    // Fallback to browser language detection
    const lang = navigator.language || navigator.languages[0]
    if (lang.startsWith('ja')) return 'ja'
    if (lang.startsWith('zh')) return 'zh'
    if (lang.startsWith('ko')) return 'ko'
    return 'en'
  }

  const [currentLanguage, setCurrentLanguage] = useState(() => {
    const langCode = getSavedLanguage()
    return languages[langCode] || languages.en
  })

  const changeLanguage = (languageCode) => {
    if (languages[languageCode]) {
      setCurrentLanguage(languages[languageCode])
      // Save to localStorage for persistence
      try {
        localStorage.setItem('esim_language', languageCode)
      } catch (error) {
        console.log('Could not save language preference')
      }
      
      // Update page title dynamically
      updatePageTitle(languages[languageCode].pageTitle)
    }
  }

  // Function to update page title
  const updatePageTitle = (title) => {
    if (typeof document !== 'undefined') {
      document.title = title
      // Also update the title element if it exists
      const titleElement = document.getElementById('page-title')
      if (titleElement) {
        titleElement.textContent = title
      }
    }
  }

  // Apply font based on language
  useEffect(() => {
    const applyFont = () => {
      const fontMap = {
        'en': '"Poppins", sans-serif',
        'ja': '"Noto Sans JP", sans-serif',
        'zh': '"Noto Sans SC", sans-serif',
        'ko': '"Noto Sans KR", sans-serif'
      }
      
      const fontFamily = fontMap[currentLanguage.code] || fontMap.en
      document.documentElement.style.setProperty('--language-font', fontFamily)
      document.body.style.fontFamily = fontFamily
    }

    applyFont()
    
    // Update page title when language changes
    updatePageTitle(currentLanguage.pageTitle)
  }, [currentLanguage])

  // Updated t() function to handle nested keys
  const t = (key) => {
    // Split the key by dots to handle nested properties
    const keys = key.split('.')
    // Start from the translations object
    let value = currentLanguage.translations

    // Traverse the nested properties
    for (const k of keys) {
      if (value && value[k] !== undefined) {
        value = value[k]
      } else {
        // If the key doesn't exist, return the key itself
        return key
      }
    }

    return value
  }

  return (
    <LanguageContext.Provider
      value={{
        currentLanguage,
        changeLanguage,
        t,
        languages: Object.values(languages),
      }}
    >
      {children}
    </LanguageContext.Provider>
  )
}

// Custom hook to use the language context
export const useLanguage = () => {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider')
  }
  return context
}